package FinancialSystem.trans;

public class GoodsInfo {
    private int id;
    private String name;
    private String type;
    private int price;
    public GoodsInfo(){

    }
    public GoodsInfo(int id,String name,String type,int price){
        this.id = id;
        this.name = name;
        this.type = type;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
